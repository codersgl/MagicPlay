        assert len(empty_results) == 0

        # Test 2: Experiment tracker error handling
        # Get non-existent experiment
        non_existent_exp = tracker.get_experiment("non-existent-id")
        assert non_existent_exp is None

        # Get results for non-existent experiment
        empty_results = tracker.get_experiment_results("non-existent-id")
        assert len(empty_results) == 0

        # Update status of non-existent experiment
        success = tracker.update_experiment_status("non-existent-id", ExperimentStatus.COMPLETED)
        assert success is False

        # Test 3: Experiment with failures
        config = ExperimentConfig(name="Failure Test", parameters={"test": "failure"})
        exp_id = tracker.create_experiment(config, status=ExperimentStatus.RUNNING)

        # Record failed result
        failed_result = ExperimentResult(
            experiment_id=exp_id,
            config=config,
            resource_record=None,
            evaluation_result=None,
            total_cost=0.5,
            total_time=10.0,
            attempts=3,
            success=False,
            error_message="Generation failed: timeout",
        )

        tracker.record_result(exp_id, failed_result)
        tracker.update_experiment_status(exp_id, ExperimentStatus.FAILED)

        # Verify failure is recorded
        experiment = tracker.get_experiment(exp_id)
        assert experiment is not None
        assert experiment["status"] == "failed"

        results = tracker.get_experiment_results(exp_id)
        assert len(results) == 1
        assert results[0].success is False
        assert "timeout" in results[0].error_message

        # Test 4: Partial success scenario
        config2 = ExperimentConfig(name="Partial Success Test", parameters={"test": "partial"})
        exp_id2 = tracker.create_experiment(config2, status=ExperimentStatus.RUNNING)

        # Record multiple results with mixed success
        for i in range(3):
            success = i < 2  # First two succeed, third fails
            result = ExperimentResult(
                experiment_id=exp_id2,
                config=config2,
                resource_record=None,
                evaluation_result=(
                    EvaluationResult(
                        score=80.0 - i * 10,
                        quality_level=QualityLevel.GOOD,
                        metrics={"test_metric": 70.0 + i * 5},
                        issues=["Minor issue"] if i == 0 else [],
                        recommendations=["Improve quality"] if i == 0 else [],
                    )
                    if success
                    else None
                ),
                total_cost=float(i + 1) * 1.0,
                total_time=float(i + 1) * 15.0,
                attempts=i + 1,
                success=success,
                error_message="Failed" if not success else None,
            )
            tracker.record_result(exp_id2, result, run_number=i + 1)

        tracker.update_experiment_status(exp_id2, ExperimentStatus.COMPLETED)

        # Verify mixed results are handled
        results2 = tracker.get_experiment_results(exp_id2)
        assert len(results2) == 3
        success_count = sum(1 for r in results2 if r.success)
        assert success_count == 2

        # Test 5: Analysis with mixed results
        analysis = tracker.analyze_experiments()
        assert analysis["total_experiments"] >= 2

        # Config2 should show 2 successful runs out of 3
        if config2.config_id in analysis["configurations"]:
            config_data = analysis["configurations"][config2.config_id]
            assert config_data["total_runs"] == 3
            assert config_data["successful_runs"] == 2

        # Test 6: Recommendation with limited data
        # Create a new tracker with no experiments
        empty_tracker = ExperimentTracker(db_path=tmp_path / "empty_tracker.db")
        no_recommendation = empty_tracker.recommend_configuration(target_quality=70.0)
        assert no_recommendation is None


